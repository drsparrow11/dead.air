let activeIndex = 0;

const trackList = document.querySelector("#trackList");
const galleryGrid = document.querySelector("#galleryGrid");
const trackArt = document.querySelector("#trackArt");
const trackNumber = document.querySelector("#trackNumber");
const trackTitle = document.querySelector("#trackTitle");
const trackTheme = document.querySelector("#trackTheme");
const trackSummary = document.querySelector("#trackSummary");
const lyricsText = document.querySelector("#lyricsText");
const audioPlayer = document.querySelector("#audioPlayer");
const youtubeTrack = document.querySelector("#youtubeTrack");
const nextTrack = document.querySelector("#nextTrack");

function formatLyrics(rawLyrics) {
  const stanzas = rawLyrics
    .trim()
    .split(/\n\s*\n/)
    .map((stanza) => stanza.trim())
    .filter(Boolean);

  lyricsText.innerHTML = stanzas.map((stanza) => `
    <p>${stanza.split("\n").map((line) => line.trim()).join("<br>")}</p>
  `).join("");
}

function renderTrackList() {
  trackList.innerHTML = tracks.map((track, index) => `
    <button class="track-button ${index === activeIndex ? "is-active" : ""}" type="button" data-index="${index}" aria-current="${index === activeIndex ? "true" : "false"}">
      <b>${track.n}</b>
      <span>${track.title}</span>
      <i aria-hidden="true"></i>
    </button>
  `).join("");
}

function setTrack(index, shouldPlay = false) {
  activeIndex = (index + tracks.length) % tracks.length;
  const track = tracks[activeIndex];
  trackArt.src = track.image;
  trackArt.alt = `${track.title} cover art`;
  trackNumber.textContent = `${track.n} // ${track.title}`;
  trackTitle.textContent = track.title;
  trackTheme.textContent = `Theme: ${track.theme}`;
  trackSummary.textContent = track.summary;
  formatLyrics(track.lyrics);
  audioPlayer.src = track.audio;
  youtubeTrack.href = track.youtube;
  renderTrackList();
  if (shouldPlay) {
    audioPlayer.play().catch(() => {});
  }
}

function renderGallery() {
  galleryGrid.innerHTML = tracks.map(track => `
    <figure>
      <img src="${track.image}" alt="${track.title} cover art" loading="lazy">
      <figcaption>${track.n} // ${track.title}</figcaption>
    </figure>
  `).join("");
}

trackList.addEventListener("click", (event) => {
  const button = event.target.closest("button[data-index]");
  if (!button) return;
  setTrack(Number(button.dataset.index), false);
});

nextTrack.addEventListener("click", () => setTrack(activeIndex + 1, true));
audioPlayer.addEventListener("ended", () => setTrack(activeIndex + 1, true));

renderGallery();
setTrack(0, false);
